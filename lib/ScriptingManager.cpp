//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ScriptingManager::pScriptEngine_type
ScriptingManager::createScriptEngine(const std::string &_name)
{
    std::lock_guard<std::mutex> guard(this->m_scriptEngineCache.getLock());

    pScriptEngine_type pScriptEngine(this->m_scriptEngineCache.getCachedService(_name));

    if( pScriptEngine.isValid() )
    {
        return pScriptEngine;
    }

    I_ScriptEngineFactory* pFactory = this->m_scriptEngineCache.getFactory(_name);

    if( pFactory == NULL )
    {
        throw Utility::runtime_exception("Zen::Scripting::ScriptingManager::createScriptEngine() : Cannot get factory instance.");
    }

    return this->m_scriptEngineCache.cacheService( _name, pFactory->create(_name) );
}

